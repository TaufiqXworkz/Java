//2. the given number is multiple of 5 or not

public class Check2Number{

	public static void main(String args[]){
	
		int num = 25;
		
		if(num  % 5 == 0){
			System.out.println("The number is multiple of 5 = "+num);
		}else{
			System.out.println("The number is not multiple of 5 = "+num);		
		}
	}
}